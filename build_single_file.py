def main() -> None:
    template = (TEMPLATES / "audit_card.html").read_text(encoding="utf-8")
    if "'''" in template:
        raise RuntimeError(
            "audit_card.html contains triple-single-quotes — build script "
            "uses ''' as the embedding fence; switch to base64 encoding."
        )

    header = HEADER.replace("__AUDIT_CARD_TEMPLATE__", template)

    parts: list[str] = [header]
    for mod_name in MODULE_ORDER:
        src_path = SRC / f"{mod_name}.py"
        if not src_path.exists():
            print(f"  WARN: {src_path} not found, skipping")
            continue
        src = src_path.read_text(encoding="utf-8")
        src = strip_relative_imports(src)
        if mod_name == "evidence_report":
            src = patch_evidence_report(src)
        if "'''" in src:
            raise RuntimeError(
                f"{mod_name}.py contains triple-single-quotes — switch this "
                "module to a different embedding scheme."
            )
        # Raw triple-quoted string can't end with an unbalanced backslash; the
        # tokenizer treats the final backslash as escaping the closing quote.
        if src.endswith("\\"):
            raise RuntimeError(
                f"{mod_name}.py ends with a backslash — incompatible with "
                "raw triple-quoted embedding."
            )

        deps_list = DEPS.get(mod_name, [])
        dep_entries = [f'"{d}": _pkg.{d}' for d in deps_list]
        # evidence_report needs the embedded template injected as a module
        # global because its module-level Jinja init reads it eagerly.
        if mod_name == "evidence_report":
            dep_entries.append('"_AUDIT_CARD_TEMPLATE": _AUDIT_CARD_TEMPLATE')
        deps_dict = "{" + ", ".join(dep_entries) + "}"

        parts.append(f"\n\n# ════════════════════ MODULE: {mod_name} ════════════════════\n")
        # `r'''...'''` preserves backslash literals in the embedded source
        # (e.g., regex patterns, `.replace("\\", "_")`). The triple-quote
        # invariant is enforced above.
        parts.append(f"_src_{mod_name} = r'''")
        parts.append(src)
        parts.append("'''\n")
        parts.append(f"{mod_name} = _load('{mod_name}', _src_{mod_name}, {deps_dict})\n")

    parts.append(FOOTER)
    out_text = "".join(parts)

    OUT.write_text(out_text, encoding="utf-8")
    line_count = out_text.count("\n")
    print(f"Wrote {OUT} ({len(out_text):,} chars, {line_count:,} lines)")


if __name__ == "__main__":
    main()
