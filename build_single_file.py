"""
Build solution.py — the single-file hackathon deliverable.

The modular `src/` tree is the development surface. This script concatenates
every `src/*.py` into one self-contained `solution.py` that judges can run
directly. Run before each submission. Do NOT edit solution.py by hand.

Strategy:
  1. Each module's source is exec'd into a freshly-created `types.ModuleType`
     whose attributes are pre-populated with its sibling-module dependencies.
     So `from . import config` lines can be stripped while `config.LLM_MODEL`
     references still resolve.
  2. The synthetic `__file__` for each module is set to a path under a
     virtual `src/` sibling of solution.py, so `config.PROJECT_ROOT`'s
     `Path(__file__).resolve().parent.parent` math comes out identical to
     the dev tree.
  3. `evidence_report.py` gets a targeted text substitution: Jinja's
     FileSystemLoader → DictLoader with the audit_card template embedded.
"""
from __future__ import annotations

import re
from pathlib import Path

ROOT = Path(__file__).resolve().parent
SRC = ROOT / "src"
TEMPLATES = ROOT / "templates"
OUT = ROOT / "solution.py"

# Topological order — leaves first, pipeline (which imports everything) near
# the end, mock_seed last (imports extract_params).
MODULE_ORDER = [
    "config",
    "ingest",
    "extract_text",
    "segment_brand",
    "llm_client",
    "step_graph",
    "extract_params",
    "validate",
    "access_score",
    "evidence_report",
    "visualize",
    "pipeline",
    "holdout",
    "mock_seed",
]

# Which sibling modules each module imports from (`from . import X`).
DEPS = {
    "config": [],
    "ingest": ["config"],
    "extract_text": ["config"],
    "segment_brand": ["config", "extract_text"],
    "llm_client": ["config"],
    "step_graph": ["config"],
    "extract_params": ["config", "llm_client"],
    "validate": ["config", "step_graph"],
    "access_score": ["config", "step_graph"],
    "evidence_report": ["config", "access_score"],
    "visualize": ["config"],
    "pipeline": [
        "config", "ingest", "extract_text", "segment_brand",
        "extract_params", "step_graph", "validate", "access_score",
        "llm_client",
    ],
    "holdout": ["config", "ingest", "pipeline"],
    "mock_seed": [
        "config", "extract_params", "extract_text", "ingest",
        "llm_client", "segment_brand",
    ],
}


def strip_relative_imports(src: str) -> str:
    """Remove `from . import …` lines. Deps are injected into the module
    namespace before exec, so these imports become unnecessary."""
    out_lines: list[str] = []
    in_paren_block = False
    for line in src.splitlines():
        if in_paren_block:
            if ")" in line:
                in_paren_block = False
            continue
        stripped = line.lstrip()
        if re.match(r"from\s+\.\s+import\s+\(", stripped):
            if ")" not in stripped:
                in_paren_block = True
            continue
        if re.match(r"from\s+\.\s+import\s+\S", stripped):
            continue
        if re.match(r"from\s+\.\w+\s+import\s+", stripped):
            continue
        out_lines.append(line)
    return "\n".join(out_lines)


def patch_evidence_report(src: str) -> str:
    """Re-route Jinja from FileSystemLoader → DictLoader so the embedded
    template constant is used instead of templates/audit_card.html."""
    src = src.replace(
        "from jinja2 import Environment, FileSystemLoader, select_autoescape",
        "from jinja2 import Environment, DictLoader, select_autoescape",
    )
    src = src.replace(
        "loader=FileSystemLoader(str(config.TEMPLATES_DIR))",
        'loader=DictLoader({"audit_card.html": _AUDIT_CARD_TEMPLATE})',
    )
    return src


