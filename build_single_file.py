# Module registry. Each `from . import X` line in src/* was stripped by the
# build; the X attribute is injected here directly so references like
# `config.LLM_MODEL` still resolve inside the exec\'d module body.
_pkg = _types.ModuleType("solution_pkg")
_sys.modules["solution_pkg"] = _pkg


def _make_module(name, deps):
    mod = _types.ModuleType(f"solution_pkg.{name}")
    mod.__file__ = str(_VIRTUAL_SRC / f"{name}.py")
    for dep_name, dep_obj in deps.items():
        setattr(mod, dep_name, dep_obj)
    _sys.modules[f"solution_pkg.{name}"] = mod
    setattr(_pkg, name, mod)
    return mod


def _load(name, src, deps):
    mod = _make_module(name, deps)
    exec(compile(src, f"<{name}>", "exec"), mod.__dict__)
    return mod

